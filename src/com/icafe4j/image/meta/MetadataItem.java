package com.icafe4j.image.meta;

public final class MetadataItem {
	
	private String key;
	private String value;

	public MetadataItem(String key, String value) {
		this.key = key;
		this.value = value;
	}
	
	public String getKey() {
		return key;
	}
	
	public String getValue() {
		return value;
	}
}
